    private static final LinkedList<Connection> DB_POOL = new LinkedList<>();

    private static final int MAX_POOL_SIZE = 3;

    static {
        for (int i = 0; i < MAX_POOL_SIZE; i++) {
            DB_POOL.addLast(new DBConnection());
        }
    }

    public Connection fetchConnection(long millis) throws InterruptedException {
        synchronized (DB_POOL) {
            if (millis < 0) {
                while (DB_POOL.isEmpty()) {
                    DB_POOL.wait(); // 无限时间的等待
                }
                return DB_POOL.removeFirst();
            } else {
                long overTime = System.currentTimeMillis() + millis;
                long remain = millis;
                while (DB_POOL.isEmpty() && remain > 0) {
                    DB_POOL.wait(remain); // 指定时间的等待
                    remain = overTime - System.currentTimeMillis();
                }
                if (!DB_POOL.isEmpty()) {
                    return DB_POOL.removeFirst();
                }
                return null;
            }
        }
    }

    public void releaseConnection(Connection connection) {
        if (connection != null) {
            synchronized (DB_POOL) {
                DB_POOL.addLast(connection);
                DB_POOL.notifyAll(); // 唤醒所有等待的线程
            }
        }
    }

